    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        buku bukur = new buku();
        try {
            String pNama = nama.getText();
            String pPenulis = penulis.getText();
            int pTahun = Integer.parseInt(tahunterbit.getText());
            int pAlur = Integer.parseInt(alur.getText());
            int ptataBahasa = Integer.parseInt(tatabahasa.getText());
            int pOrisinalitas = Integer.parseInt(ori.getText());
            float pRating = (pAlur + ptataBahasa +pOrisinalitas) / 3;
        
            bukur.id = cursID;
            bukur.judul = pNama;
            bukur.penulis = pPenulis;
            bukur.tahun = pTahun;
            bukur.alur = pAlur;
            bukur.bahasa = ptataBahasa;
            bukur.ori = pOrisinalitas;
            bukur.rating = pRating;
            
            uiControl control = new uiControl();
            control.upBuku(bukur);
        }catch(Exception ex){
            
        }finally{
            JOptionPane.showMessageDialog(null, "TERUPDATE!");
            dispose();
            new mainView();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

