    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        Baris = jTable1.getSelectedRow();
        uiControl control = new uiControl();
        List<buku> bukus = control.getdata();
        buku selectedBook = bukus.get(Baris);
        cursID = selectedBook.id;
        String namaZ = selectedBook.judul;
        String penulisZ = selectedBook.penulis;
        int tahunTZ = selectedBook.tahun;
        int alurZ = selectedBook.alur;
        int TBZ = selectedBook.bahasa;
        int orisi = selectedBook.ori;
            
        nama.setText(namaZ);
        penulis.setText(penulisZ);
        tahunterbit.setText(String.valueOf(tahunTZ));
        alur.setText(String.valueOf(alurZ));
        tatabahasa.setText(String.valueOf(TBZ));
        ori.setText(String.valueOf(orisi));
    }//GEN-LAST:event_jTable1MouseClicked

